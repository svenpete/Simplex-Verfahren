package Gui;

public class Nebenbedingungen
{
    private double d1;
    private double d2;
    private double d3;
    private double d4;

    public Nebenbedingungen(double d1)
    {
        this.d1=d1;

    }
    public Nebenbedingungen(double d1, double d2)
    {
        this.d1=d1;
        this.d2=d2;
    }
    public Nebenbedingungen(double d1, double d2, double d3)
    {
        this.d1=d1;
        this.d2=d2;
        this.d3=d3;

    }
    public Nebenbedingungen(double d1, double d2, double d3, double d4)
    {
        this.d1=d1;
        this.d2=d2;
        this.d3=d3;
        this.d4=d4;

    }
}
